char	*ft_rot42(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i])
	{
		c = str[i];
		if (c >= 'A' && c <= 'Z')
		{
			c = c + 16;
			if (c > 'Z')
				c = c - 26;
		}
		else if (c >= 'a' && c <= 'j')
			c = c + 16;
		else if (c >= 'k' && c <= 'z')
			c = c - 10;
		str[i] = c;
		i++;
	}
	return (str);
}
